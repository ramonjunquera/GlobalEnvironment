#Los operadores binarios son los que realizan operaciones binarias con valores enteros.

v1=0b01 #1 3n notación binaria
v2=0b10 #2
print("suma",v1+v2) # 1+2=3
print("&",v1 & v2) # 1 & 2 = 0
print("|",v1 | v2) # 1 | 2 = 3
print("2>>1",v2>>1) # 2>>1 = 1
print("2<<1",v2<<1) # 2<<1 = 4
print("2^1",v2 ^ v1) # 2 xor 1 = 3
