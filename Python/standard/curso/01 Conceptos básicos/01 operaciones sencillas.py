#Descripción de operaciones básicas
# Suma
print(2+2) #Resultado: 4
# Resta
print(3-2) #Resultado: 1
# Producto
print(3*2) #Resultado: 6
# División
print(1/2) #Resultado 0.5
# Potencia
print(2**8) #Resultado: 2^8=256
# División entera
print(7//3) #Resultado: int(7/3)=2
# Módulo o resto
print(7%3) #Resultado: 7/3 = 2 con resto 1
# Paréntesis
print((3+2)*4) #Resultado: 20
# Negativos
print((-7+2)*(-4)) #Resultado: -5*(-4)=20
# También se admiten números en notación hexadecimal
print(0xFF+1) #Resultado: 255+1=256
# Y notación binaria
print(0b01 + 0b10) #Resultado 1+2=3