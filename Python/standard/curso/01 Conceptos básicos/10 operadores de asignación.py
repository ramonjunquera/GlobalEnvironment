# Como en C se puede utilizar operadores de asignación
x=2
x+=3
print(x) #Resultado: 5
# Es lo mismo que...
x=2
x=x+3
print(x) #Resultado: 5
# También funcionan con el resto de operadores
# División
x=8
x/=2
print(x) #Resultado: 4
# Multiplicación
x=3
x*=2
print(x) #Resultado: 6
# Potenciación
x=2
x**=8
print(x) #Resultado: 256
# División entera
x=8
x//=3
print(x) #Resultado: 2
# Resto
x=10
x%=3
print(x) #Resultado: 1

# Operaciones con cadenas
# Concatenación
x='uno'
x+='Dos'
print(x) #Resultado: unoDos
x='no'
x*=3
print(x) #Resultado: nonono
