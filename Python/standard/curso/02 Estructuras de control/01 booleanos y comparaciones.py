# Asignación de valores a variables booleanas
# Es impotante que los valores booleanos se escriban correctamente
# como True o False. Con la inicial en mayúscula
myBool=True
print(myBool) #Resultado: True
myBool=False
print(myBool) #Resultado: False

# Las comparativas también dan resultados booleanos
print(2==3) #Resultado: False
print("hola"=='hola') #Resultado: True
# Comparación de distinto que
print("uno"!="dos") #Resultado: False
# Comparación de mayor y menor
print(7>5) #Resultado: True
print(10<10) #Resultado: False
print(7>7.0) #Resultado: False
# Comparación de igual o mayor/menor que
print(7<=8) #Resultado: True
print(9>=9.0) #Resultado: True
