# Pitonicidad. PEP

# PEP = Python Enhancement Proposals = propuestas de mejora para Python

# Reglas generales de sintaxis:
# - Los módulos deben tener nombre cortos y en minúsculas
# - Los nombres de las clases deben estar en estilo CamelCase
# - Las variables deben estar en minúsculas_con_subguiones
# - Las constantes deben estar en MAYUSCULAS_CON_SUBGUIONES
# - Los nombres que pueden causar conflicto con las palabras claves deben
#   tener como prefijo un subguión
# - Las lineas no deberían ser más largas de 80 caracteres
# - Cada línea debería tener sólo una sentencia
# - Utilizar espacios y no tabulaciones para identar
# - Una identación consta de 4 espacios
# - Un comentario en la misma linea del código debe separarse por dos espacios
# - Un comentario debe separarse de un espacio de #
# - La importación de métodos se debe realizar al comienzo del código, primero se importan los módulos
#   propios de Python, después los de terceros y por último los creados por el usuario. Si hay varios,
#   en orden alfabético. Entre cada bloque de imports se debejará una línea en blanco.
# - Al especificar valores por defecto en los parámetros de una función, no se deben dejar espacios
#   ni antes ni después del signo =
